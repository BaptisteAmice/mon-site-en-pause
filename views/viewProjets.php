          <h4>CONCEPTION GOUTTIÈRE À PROPRETÉ OPTIMALE</h4>
          <p>Le but de ce projet était la création d'une gouttière capable de se replier en cas de beau temps, pour se mettre à couvert et se protéger des chutes de feuilles et autres saletés. Dans une équipe de trois membre et sur près d'une année, de nombreuses tâches ont été réalisées. Nous avons tous participé à la recherche sur l'aspect viable du projet, la rédaction du cahier des charges et du GANTT. Ensuite, ma part du travail a été la sélection des pièce dépendant de normes, mais surtout de calculs de forces (un point qui a demandé de nombreux essais, car les forces dépendent des matières utilisées, mais les matières dépendent elles des forces). Les pièces ont ensuite pu être commandées mais la réalisation de la maquette prévue n'a jamais pu être réalisée dû au confinement. Les matériaux et dimensions de la maquette étant différent d'une situation réelle, il a fallu réaliser des calculs supplémentaires. Nous nous sommes donc contenté d'une modélisation 3D implémentant des forces (via Meca3d SolidWorks). Finalement, ce projet s'est achevé par une présentation.</p>
